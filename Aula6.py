import  math
#para inmportar somente uma função da biblioteca usa-se o comando: from biblioteca import função

#função arredonda pra cima
print(math.ceil(10.3))

#função arredonda pra baixo
print(math.floor(15.7))

#função potencia
print(math.pow(5,2))
#ou
print(5**2)

#funçã fatorial
print(math.factorial(5))

#função da raiz quadrada
print(math.sqrt(16))

#funcao mdc -> Maior Divisor Comun
print(math.gcd(10,5))