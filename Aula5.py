#Manipulação de Strings

S = 'Palavra ou frase'

#Tamanho
print(len(S))

#Troca palavras ou caracteres em uma string
print(S.replace('Palavra','Samuel'))

#Verifica se uma String contém determinada palavra ou frase
print("ou" in S)

#Split - o parametro diz onde a string vai ser dividida
print(S.split())